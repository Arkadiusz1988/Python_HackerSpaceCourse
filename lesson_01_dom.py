# Pierwszy program
# Komentowanie
# Użycie funkcji print otaz cudzysłowów

print('Pierwszy program')

# Wiele parametrów oraz konkatencja łańcuchów
print('To jest', 'mój', 'pierwszy ' + 'program')

# Parametry mogą być umeiszczone w kolejnych liniach
print('To jest',
      'mój',
      'pierwszy ' + 'program')

# Znak \ umożliwia kontynowanie instrukcji w kolejnym wierszu
print('Dokańczanie instrukcji', \
      'w kolejnym wierszu')

# Zastosowanie potrójnych cudzysłowów
print('''Oto jest bardzo, bardzo, bardzo
niezwykle długi, oj taaaaaaaaki długi,
długi, długi, długi, długi 
tekst, że hej''')

# Definiowanie łańcucha końcowego przez parametr end
print('Oto', end=' ')
print('on...')

# Zastosowanie różnych typów cudzysłowiów
print('Jacek "Wielki" Programista')

# Zastosowanie sekwencji specjalnych
print('\aJacek \n"Wielki" \n\t\tProgramista')

# Powielanie łańcucha
print('Koniec!' * 3)

